/** Card primitive. Used by molecules for cards and panels. */
import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-2xl border border-border/50 bg-card/95 backdrop-blur-xl text-card-foreground shadow-card transition-all duration-300 hover:shadow-card-hover hover:-translate-y-1 relative overflow-hidden",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

